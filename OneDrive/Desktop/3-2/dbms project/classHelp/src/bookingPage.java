    private void performBooking(int slotId, int roomId, int userId, java.util.Date date) {
    String sql = "INSERT INTO bookings (SlotID, RoomID, UserID, Date) VALUES (?, ?, ?, ?)";
    try (Connection conn = DatabaseConnector.connect();
         PreparedStatement pstmt = conn.prepareStatement(sql)) {
        pstmt.setInt(1, slotId);
        pstmt.setInt(2, roomId);
        pstmt.setInt(3, userId);
        pstmt.setDate(4, new java.sql.Date(date.getTime()));

        int affectedRows = pstmt.executeUpdate();
        if (affectedRows > 0) {
            JOptionPane.showMessageDialog(this, "Booking successful!");
        } else {
            JOptionPane.showMessageDialog(this, "Booking failed.");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error during booking: " + e.getMessage());
        e.printStackTrace();
    }
}
    
    
