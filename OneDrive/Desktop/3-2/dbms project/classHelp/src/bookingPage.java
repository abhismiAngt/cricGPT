    private void updateRoomStatus(java.util.Date selectedDate, int selectedRoomId) {
        
        
        PanelArray[0] = jPanel23;
        PanelArray[1] = jPanel24;
        PanelArray[2] = jPanel25;
        PanelArray[3] = jPanel26;
        PanelArray[4] = jPanel27;
        PanelArray[5] = jPanel28;
        PanelArray[6] = jPanel29;
        PanelArray[7] = jPanel30;
        PanelArray[8] = jPanel31;
        PanelArray[9] = jPanel32;
        PanelArray[10] = jPanel33;
        PanelArray[11] = jPanel34;
        
        
        
    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
    SimpleDateFormat dayFormat = new SimpleDateFormat("EEEE"); // Returns the day name like "Monday"
    String formattedDate = dateFormat.format(selectedDate);
    String dayOfWeek = dayFormat.format(selectedDate);

    try (Connection conn = DatabaseConnector.connect();
         PreparedStatement stmt = conn.prepareStatement(
             "SELECT SlotID FROM Timetable WHERE RoomID = ? AND DayOfWeek = ? " +
             "UNION " +
             "SELECT SlotID FROM Bookings WHERE RoomID = ? AND Date = ?")) {
        
        stmt.setInt(1, selectedRoomId);
        stmt.setString(2, dayOfWeek);
        stmt.setInt(3, selectedRoomId);
        stmt.setString(4, formattedDate);
        
        ResultSet rs = stmt.executeQuery();

        Set<Integer> occupiedSlots = new HashSet<>();
        while (rs.next()) {
            occupiedSlots.add(rs.getInt("SlotID"));
        }

        // Assuming jPanelArray is an array or list of your JPanel objects representing the time slots
        for (int i = 0; i < PanelArray.length; i++) {
            if (occupiedSlots.contains(i + 1)) { // Slot IDs assumed to start at 1 and match index + 1
                PanelArray[i].setBorder(BorderFactory.createLineBorder(Color.RED, 3));
            } else {
                PanelArray[i].setBorder(BorderFactory.createLineBorder(Color.GREEN, 3));
            }
            PanelArray[i].repaint();
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
    }
    
    
    
    

  
